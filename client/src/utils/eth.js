export async function connect() {
  web3Modal = new Web3Modal({
    cacheProvider: true, // optional
    providerOptions: {} // required
  });

  provider = await web3Modal.connect();
  const web3Provider = new ethers.providers.Web3Provider(provider);
  signer = web3Provider.getSigner();
  contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
}

export async function getAchievements(address) {
  return await contract.getAchievements(address);
}

export async function createAchievement(recipient, name, description, metadataURI) {
  return await contract.createAchievement(recipient, name, description, metadataURI);
}
